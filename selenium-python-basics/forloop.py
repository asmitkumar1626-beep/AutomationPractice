def addition(x,y):
    '''
    :param x:
    :param y:
    :return:
    '''
    return(x+y)
def substraction(x,y):
    '''
    :param x:
    :param y:
    :return:
    '''
    return(x-y)
def multiplication(x,y):
    '''
    :param x:
    :param y:
    :return:
    '''
    return(x*y)
def division(x,y):
    '''
    :param x:
    :param y:
    :return:
    '''
    return(x/y)

a=addition(3,4)
m=multiplication(4,6)
d=division(4,2)
s=substraction(99,4)
print(a,s,m,d)